    private void btn_dnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dnActionPerformed
        // TODO add your handling code here:
        String username = txt_username.getText();
        String password = txt_pass.getText();
        try
        {
            InetAddress localHost = InetAddress.getLocalHost();
            String tenmay = localHost.getHostName();
            SystemInfo systemInfo = new SystemInfo();
            CentralProcessor cpu = systemInfo.getHardware().getProcessor();
            String tencpu = cpu.getProcessorIdentifier().getName();
            OperatingSystem hedieuhanh = systemInfo.getOperatingSystem();
            String os = hedieuhanh.getFamily();
            //Lay ram
            SystemInfo si = new SystemInfo();
            HardwareAbstractionLayer hal = si.getHardware();
            GlobalMemory memory = hal.getMemory();
            String serial1 = null, serial2 = null;
            int i = 0;
            for (PhysicalMemory pm : memory.getPhysicalMemory()) {
                if (i == 0) serial1 = pm.getSerialNumber();
                else if (i == 1) serial2 = pm.getSerialNumber();
                i++;
            }
            if (i == 1) serial2 = null;
            String ram1 = (serial1 != null ? serial1 : "NULL");
            String ram2 = (serial2 != null ? serial2 : "NULL");
            //Lấy rom
            List<HWDiskStore> disks = hal.getDiskStores();
            String serialRom1 = null, serialRom2 = null;
            int j = 0;
            for (HWDiskStore disk : disks) {
                if (j == 0) serialRom1 = disk.getSerial();
                else if (j == 1) serialRom2 = disk.getSerial();
                j++;
            }
            if (j == 1) serialRom2 = null;
            String rom1 = (serialRom1 != null ? serialRom1 : "NULL");
            String rom2 = (serialRom2 != null ? serialRom2 : "NULL");
            
            mycls cls = new mycls();
            username = cls.mahoa(username);
            username = username.replace("+", "%2B");
            password = cls.mahoa(password);
            password = password.replace("+", "%2B");
            tenmay = cls.mahoa(tenmay);
            tenmay = tenmay.replace("+", "%2B");
            tencpu = cls.mahoa(tencpu);
            tencpu = tencpu.replace("+", "%2B");
            os = cls.mahoa(os);
            os = os.replace("+", "%2B");
            ram1 = cls.mahoa(ram1);
            ram1 = ram1.replace("+", "%2B");
            ram2 = cls.mahoa(ram2);
            ram2 = ram2.replace("+", "%2B");
            rom1 = cls.mahoa(rom1);
            rom1 = rom1.replace("+", "%2B");
            rom2 = cls.mahoa(rom2);
            rom2 = rom2.replace("+", "%2B");
            
            String thamso = "username="+username+"&password="+password+"&ram1="+ram1+"&ram2="+ram2+"&rom1="+rom1+"&rom2="+rom2+"&tenmay="+tenmay+"&tencpu="+tencpu+"&os="+os+"";
            String url = Constants.API_LOGIN+thamso;

            
            JSONArray jarr = new JSONArray();
            jarr = cls.docapi(url);
            for(int it=0;it<cls.docapi(url).length();it++)
            {
                try {
                    JSONObject jon = jarr.getJSONObject(it);
                    String iduser = String.valueOf(jon.getInt("iduser")); 
                    int response = jon.getInt("Response");
                    int pq = jon.getInt("PQ");
                    String name = jon.getString("name");
                    if(response == 101 && pq == 1)
                    {
                        
                        formDangKy fm = new formDangKy(iduser,name);
                        fm.setVisible(true);
                        this.setVisible(false);
                        this.dispose();
                    }
                    else if(response == 102 && pq == 1)
                    {
                        String token = jon.getString("token");
                        long time = jon.getLong("time");
                        cls.ghifile(token,time);
                        
                        myform mf = new myform(iduser,name);
                        mf.setVisible(true);
                        this.setVisible(false);
                        this.dispose();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Bạn không có quyền truy cập");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            
        }catch(Exception e)
        {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btn_dnActionPerformed

